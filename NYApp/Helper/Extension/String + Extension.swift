import Foundation
import Localize_Swift

extension String{
    static var NYTimesMostPopular:String{
        return "NY Times Most Popular".localized()
    }
    
    static var noInternetAccess:String{
        return "noInternetAccess".localized()
    }
    
    static var error:String{
        return "error".localized()
    }
    
    static var refresh:String{
        return "refresh".localized()
    }
}
